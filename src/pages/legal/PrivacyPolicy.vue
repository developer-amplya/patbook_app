            <p><strong>¿Qué es un Evaluación de Impacto de Protección de Datos (EIPD)?</strong></p>

            <p>Una Evaluación de Impacto de Protección de Datos (en adelante, la EIPD) es un proceso ligado a los
                principios de protección de datos desde el diseño y protección de datos por defecto concebido para:</p>
            <p>&nbsp;&nbsp;- describir, de manera sistemática, anticipada y preventiva, un tratamiento de datos
                personales;</p>
            <p>&nbsp;&nbsp;- evaluar su necesidad y proporcionalidad del tratamiento;</p>
            <p>&nbsp;&nbsp;- y gestionar los potenciales riesgos para los derechos y libertades derivados de las
                diferentes operaciones de tratamiento de datos personales a los que estarán expuestos, determinando las
                medidas necesarias para reducirlos hasta un nivel de riesgo aceptable.</p>


            <p><strong>¿Por qué una Evaluación de Impacto de Protección de Datos (EIPD)?</strong></p>

            <p>Porque desde EL ARTESANO DE IDEAS SL, tenemos un firme compromiso con la privacidad de las personas y con
                el cumplimiento de las normas de privacidad vigentes, y, por lo tanto, la realización exhaustiva de una
                EIPD nos permite determinar e implementar todas las medidas y garantías necesarias para reducir los
                riesgos para los derechos y libertades al máximo, garantizando la seguridad y la privacidad de los datos
                de los usuarios de la aplicación, así como garantizar el cumplimiento de la normativa de
                privacidad. </p>

            <p><strong>¿Qué medidas y garantías implementamos como consecuencia de la EIP?</strong></p>

            <p><strong>· Seudonimización</strong></p>

            <p>La aplicación de la seudonimización es una medida nuclear dentro de la EIPD para garantizar la
                protección de la seguridad y la protección de los derechos y libertades de los interesados.</p>
            <p>El objetivo de la seudonimización es proteger los datos personales ocultando la identidad de las
                personas en un conjunto de datos, por ejemplo, sustituyendo uno o más identificadores de datos
                personales por los denominados seudónimos (y protegiendo adecuadamente el vínculo entre los seudónimos y
                los identificadores iniciales).</p>
            <p>Las ventajas de utilizar la Seudonimización son:</p>

            <ul>
                <li>Permite ocultar la identidad de los individuos en el contexto de un conjunto de datos específico,
                    mejorando así su seguridad y protección de la intimidad.
                </li>
                <li>Reduce el riesgo de vinculación de los datos personales de una persona concreta en distintos
                    ámbitos del tratamiento de datos
                </li>
                <li>Es una herramienta útil que contribuye al principio de exactitud de la protección de datos.</li>
                <li>Es una herramienta útil que no sólo puede mejorar la seguridad de los datos personales, sino
                    también apoyar su cumplimiento general de los principios de protección de datos del RGPD.
                </li>
                <li>Beneficiosa para los titulares de los datos, ya que dicha protección de la intimidad contribuye a
                    crear confianza entre el responsable del tratamiento y los titulares de los datos, elemento esencial
                    para los servicios digitales.
                </li>
                <li>Reduce significativamente la probabilidad de que las personas sean identificadas en caso de
                    violación de datos.
                </li>

            </ul>

